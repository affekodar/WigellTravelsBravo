package com.example.wigelltravelsbravo.VO;

public class Observation {
    private String date;
    private double value;

    public Observation() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }
}

